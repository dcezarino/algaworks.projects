public class OperadoresAritmeticos{

	public static void main(String[] args){

		String imprimeLinha = "===================================";

		System.out.println(imprimeLinha);

		Integer adicao = 5 + 2 ;
		System.out.println("Resultado da adicao de 5 com 2: " + adicao);

		Integer subtracao = 5 - 2;
		System.out.println("Resultado da subtracao de 5 pelo 2: " + subtracao);

		Integer multiplicacao = 5 * 2;
		System.out.println("Resultado da multiplicacao de 5 pelo 2: " + multiplicacao);

		Double divisao = 5.0 / 2.0;
		System.out.println("Resultado da divisao de 5 pelo 2: " + divisao);

		Double moduloResto = 5.0 % 2.0;
		System.out.println("Resto da divisao de 5 pelo 2: " + moduloResto);

		System.out.println("Fim");
		System.out.println(imprimeLinha);



	}


}